/** Emits an idempotent candidate loader for food forms curated for recipe V3. */
import { createHash } from 'node:crypto'
import { readFileSync, writeFileSync } from 'node:fs'
import { fileURLToPath } from 'node:url'
import { dirname, join } from 'node:path'
import { normalizeName } from '../lib/normalize.mjs'

const __dirname = dirname(fileURLToPath(import.meta.url))
const OUT = join(__dirname, '..', 'out')
const catalog = JSON.parse(readFileSync(join(OUT, 'recipe-food-catalog.json'), 'utf8'))
const q = (value) => `'${String(value).replace(/'/g, "''")}'`
const qn = (value) => value == null ? 'NULL' : q(value)
const num = (value) => Number.isFinite(Number(value)) ? Number(value) : 'NULL'
const hash = createHash('md5').update(JSON.stringify(catalog.forms)).digest('hex')

const nutrientDefinitions = [
  ['energy_kcal', 'kcal', 'kcal'],
  ['protein_g', 'g', 'proteinG'],
  ['carbohydrate_g', 'g', 'carbsG'],
  ['fat_g', 'g', 'fatG'],
  ['fiber_g', 'g', 'fiberG'],
]

function conceptName(form) {
  return form.state?.concept || form.canonical_name.split(',')[0]
}

function defaultUnit(form) {
  if (form.units_used.includes('g')) return 'g'
  if (form.units_used.includes('ml')) return 'ml'
  if (form.units_used.includes('u')) return 'u'
  if (form.units_used.includes('tranche')) return 'tranche'
  return 'g'
}

let sql = `-- Recipe V3 food forms — generated by build-recipe-food-sql.mjs.
-- Candidate-only: missing storage truth remains an explicit review task.
-- configuration_hash = ${hash}

INSERT INTO ops.source_datasets
  (code, name, publisher, source_url, license_code, allowed_uses,
   update_strategy, current_version, last_checked_at)
VALUES
  ('myko_recipe_food_v3', 'Formes alimentaires résolues pour le corpus V3',
   'Myko + Anses Ciqual', 'https://ciqual.anses.fr/', 'etalab-2.0',
   '{"store_raw":true,"redistribute":true,"modify":true,"attribution_required":true}'::jsonb,
   'manual_review', 'v3-2026-07', now())
ON CONFLICT (code) DO UPDATE SET current_version = EXCLUDED.current_version, last_checked_at = now();

INSERT INTO ops.import_runs
  (source_dataset_id, source_version, code_version, configuration_hash,
   status, started_at, completed_at, candidate_count)
SELECT id, 'v3-2026-07', 'recipe-food-loader-1.0', ${q(hash)},
       'completed', now(), now(), ${catalog.forms.length}
FROM ops.source_datasets
WHERE code = 'myko_recipe_food_v3'
  AND NOT EXISTS (SELECT 1 FROM ops.import_runs WHERE configuration_hash = ${q(hash)});
`

for (const form of catalog.forms) {
  const normalized = form.canonical_name_normalized
  const concept = conceptName(form)
  const conceptNormalized = normalizeName(concept)
  const nutrientRows = nutrientDefinitions
    .filter(([, , key]) => Number.isFinite(form.per100g?.[key]))
    .map(([code, unit, key]) => `(v_profile, ${q(code)}, ${num(form.per100g[key])}, ${q(unit)}, ${q(form.source === 'myko_curated_override' ? 'measured' : 'estimated')})`)
  const profileRows = nutrientRows.length
    ? nutrientRows
    : [`(v_profile, 'energy_kcal', NULL, 'kcal', 'not_available')`]
  const conversions = [{ from: 'g', factor: 1, confidence: 'A', context: 'identité masse' }]
  if (Number.isFinite(form.conversion?.density_g_per_ml)) {
    conversions.push({ from: 'ml', factor: form.conversion.density_g_per_ml, confidence: 'B', context: 'densité alimentaire revue' })
  }
  if (Number.isFinite(form.conversion?.grams_per_unit)) {
    for (const unit of form.units_used.filter((value) => value === 'u' || value === 'tranche')) {
      conversions.push({ from: unit, factor: form.conversion.grams_per_unit, confidence: 'B', context: 'poids unitaire culinaire revu' })
    }
  }

  sql += `
DO $food$
DECLARE
  v_dataset uuid;
  v_run uuid;
  v_category uuid;
  v_concept uuid;
  v_form uuid;
  v_profile uuid;
BEGIN
  SELECT id INTO v_dataset FROM ops.source_datasets WHERE code = 'myko_recipe_food_v3';
  SELECT id INTO v_run FROM ops.import_runs WHERE configuration_hash = ${q(hash)} LIMIT 1;
  SELECT id INTO v_category FROM catalog.food_categories WHERE code = ${q(form.category || 'preparations_culinaires')};
  IF v_category IS NULL THEN
    SELECT id INTO v_category FROM catalog.food_categories WHERE code = 'preparations_culinaires';
  END IF;

  SELECT ff.id INTO v_form
  FROM catalog.food_forms ff
  WHERE ff.canonical_name_normalized = ${q(normalized)} AND ff.status <> 'rejected'
  ORDER BY CASE ff.status WHEN 'published' THEN 0 ELSE 1 END, ff.confidence_level
  LIMIT 1;

  IF v_form IS NULL THEN
    INSERT INTO catalog.food_concepts
      (canonical_name, canonical_name_normalized, category_id, status, confidence_level)
    VALUES (${q(concept)}, ${q(conceptNormalized || normalized)}, v_category, 'candidate', ${q(form.confidence)})
    ON CONFLICT (canonical_name_normalized) DO UPDATE SET updated_at = now()
    RETURNING id INTO v_concept;

    INSERT INTO catalog.food_forms
      (food_concept_id, canonical_name, canonical_name_normalized,
       physical_state, cooking_state, preservation_state, bone_state, skin_state,
       default_quantity_unit, edible_yield_ratio, status, confidence_level,
       identity_confidence, category_confidence, state_confidence)
    VALUES
      (v_concept, ${q(form.canonical_name)}, ${q(normalized)},
       ${qn(form.state?.physical_state)}, ${qn(form.state?.cooking_state)}, ${qn(form.state?.preservation_state)},
       ${qn(form.state?.bone_state)}, ${qn(form.state?.skin_state)},
       ${q(defaultUnit(form))}, 1, 'candidate', ${q(form.confidence)},
       ${q(form.confidence)}, 'B', 'B')
    RETURNING id INTO v_form;
  END IF;

  SELECT id INTO v_profile
  FROM catalog.food_nutrition_profiles
  WHERE food_form_id = v_form AND is_primary
  LIMIT 1;

  IF v_profile IS NULL THEN
    INSERT INTO catalog.food_nutrition_profiles
      (food_form_id, source_dataset_id, source_record_key, basis_quantity,
       basis_unit, data_version, confidence_level, is_primary)
    VALUES
      (v_form, v_dataset, ${q(`${form.source_record_key || 'override'}:${normalized}`)}, 100,
       'g', 'v3-2026-07', ${q(form.confidence)}, true)
    ON CONFLICT (source_dataset_id, source_record_key, data_version) DO UPDATE
      SET food_form_id = EXCLUDED.food_form_id, confidence_level = EXCLUDED.confidence_level
    RETURNING id INTO v_profile;

    DELETE FROM catalog.food_nutrient_values WHERE nutrition_profile_id = v_profile;
    INSERT INTO catalog.food_nutrient_values
      (nutrition_profile_id, nutrient_code, amount, unit, value_status)
    VALUES
      ${profileRows.join(',\n      ')};
  END IF;

  DELETE FROM catalog.food_unit_conversions
  WHERE food_form_id = v_form AND source_dataset_id = v_dataset;
`
  for (const conversion of conversions) {
    sql += `  INSERT INTO catalog.food_unit_conversions
    (food_form_id, from_unit, to_unit, factor, context, source_dataset_id, confidence_level, status)
  VALUES
    (v_form, ${q(conversion.from)}, 'g', ${num(conversion.factor)}, ${q(conversion.context)},
     v_dataset, ${q(conversion.confidence)}, 'candidate')
  ON CONFLICT (food_form_id, from_unit, to_unit, COALESCE(context, '')) DO UPDATE
    SET factor = EXCLUDED.factor, confidence_level = EXCLUDED.confidence_level;
`
  }
  sql += `
  DELETE FROM quality.review_tasks
  WHERE entity_type = 'food_form' AND entity_id = v_form AND task_type = 'missing_storage_profile';
  INSERT INTO quality.review_tasks
    (entity_type, entity_id, task_type, priority, reason_codes, proposed_changes, status)
  SELECT 'food_form', v_form, 'missing_storage_profile', 3,
         ARRAY['storage_truth_required'],
         jsonb_build_object('form', ${q(form.canonical_name)}, 'note', 'Aucune durée générique inventée.'),
         'open'
  WHERE NOT EXISTS (SELECT 1 FROM catalog.food_storage_profiles WHERE food_form_id = v_form);

  DELETE FROM ops.field_provenance
  WHERE entity_schema = 'catalog' AND entity_table = 'food_forms'
    AND entity_id = v_form AND field_name = 'recipe_v3_resolution';
  INSERT INTO ops.field_provenance
    (entity_schema, entity_table, entity_id, field_name, source_dataset_id,
     source_record_key, normalized_value, transformation_rule, import_run_id, selected)
  VALUES
    ('catalog', 'food_forms', v_form, 'recipe_v3_resolution', v_dataset,
     ${q(form.source_record_key || `override:${normalized}`)}, to_jsonb(${q(form.source_name)}::text),
     ${q(form.note ? 'explicit_proxy_with_note' : 'exact_or_reviewed_ciqual_match')}, v_run, true);
END
$food$;
`
}

writeFileSync(join(OUT, 'recipe-food-load.sql'), sql)
writeFileSync(join(OUT, 'recipe-food-load-report.json'), `${JSON.stringify({
  corpus_version: catalog.corpus_version,
  configuration_hash: hash,
  forms: catalog.forms.length,
  sql_bytes: Buffer.byteLength(sql),
}, null, 2)}\n`)
