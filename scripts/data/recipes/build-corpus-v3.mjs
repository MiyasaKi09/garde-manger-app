/**
 * Emits the idempotent SQL loader for the 302-recipe V3 editorial corpus.
 * The loader writes candidates, exact requirements, sensory contracts and
 * review tasks. It never promotes a recipe with unresolved food truth.
 */
import { createHash } from 'node:crypto'
import { readFileSync, writeFileSync, mkdirSync } from 'node:fs'
import { fileURLToPath } from 'node:url'
import { dirname, join } from 'node:path'
import { normalizeName } from '../lib/normalize.mjs'

const __dirname = dirname(fileURLToPath(import.meta.url))
const ROOT = join(__dirname, '..', '..', '..')
const OUT = join(__dirname, '..', 'out')
const corpus = JSON.parse(readFileSync(join(ROOT, 'data', 'recipes', 'corpus-v3.json'), 'utf8'))
const foodReport = JSON.parse(readFileSync(join(OUT, 'recipe-food-match-report.json'), 'utf8'))
const eligibilityByCode = new Map(foodReport.recipe_eligibility.map((item) => [item.code, item]))

const q = (value) => `'${String(value).replace(/'/g, "''")}'`
const qn = (value) => value == null ? 'NULL' : q(value)
const num = (value) => value == null || value === '' ? 'NULL' : Number(value)
const json = (value) => `${q(JSON.stringify(value))}::jsonb`
const array = (values = []) => values.length ? `ARRAY[${values.map(q).join(',')}]::text[]` : `ARRAY[]::text[]`
const hashRecipe = (recipe) => createHash('md5').update(JSON.stringify(recipe)).digest('hex')
const corpusHash = createHash('md5').update(corpus.recipes.map(hashRecipe).sort().join(',')).digest('hex')
const rulesHash = createHash('md5').update(JSON.stringify(corpus.planner_sensory_rules)).digest('hex')
const declaredYields = new Map([
  // Rendement culinaire utilisé pour dimensionner les sous-recettes.
  ['FR-024', { quantity: 870, unit: 'g' }],
])

let sql = `-- Myko V3 culinary corpus — generated by build-corpus-v3.mjs.
-- Idempotent candidate load. Requires migration 20260715170000.
-- corpus_hash = ${corpusHash}

INSERT INTO ops.source_datasets
  (code, name, publisher, source_url, license_code, allowed_uses,
   update_strategy, current_version, last_checked_at)
VALUES
  ('myko_editorial_v3', 'Corpus culinaire canonique V3 — 302 plats réels',
   'Myko', 'internal', 'editorial',
   '{"store_raw":true,"redistribute":false,"modify":true,"attribution_required":false,"own_content":true}'::jsonb,
   'manual', 'v3-300-real-dishes', now())
ON CONFLICT (code) DO UPDATE
  SET current_version = EXCLUDED.current_version, last_checked_at = now();

INSERT INTO ops.import_runs
  (source_dataset_id, source_version, code_version, configuration_hash,
   status, started_at, completed_at, candidate_count)
SELECT id, 'v3-300-real-dishes', 'v3-loader-1.0', ${q(corpusHash)},
       'completed', now(), now(), ${corpus.recipes.length}
FROM ops.source_datasets
WHERE code = 'myko_editorial_v3'
  AND NOT EXISTS (SELECT 1 FROM ops.import_runs WHERE configuration_hash = ${q(corpusHash)});

INSERT INTO culinary.recipe_planner_rule_sets
  (code, version, rules, source_dataset_id, status, content_hash)
SELECT 'myko-sensory-planner', 3, ${json(corpus.planner_sensory_rules)}, id, 'candidate', ${q(rulesHash)}
FROM ops.source_datasets WHERE code = 'myko_editorial_v3'
ON CONFLICT (code, version) DO UPDATE
  SET rules = EXCLUDED.rules, content_hash = EXCLUDED.content_hash;
`

for (const recipe of corpus.recipes) {
  const contentHash = hashRecipe(recipe)
  const eligible = eligibilityByCode.get(recipe.code)?.eligible_for_publication === true
  const declaredYield = declaredYields.get(recipe.code)
  sql += `
DO $recipe$
DECLARE
  v_family uuid;
  v_version uuid;
  v_component uuid;
  v_missing integer;
  v_static_eligible boolean := ${eligible ? 'true' : 'false'};
BEGIN
  INSERT INTO culinary.recipe_families
    (canonical_name, canonical_name_normalized, culinary_origin, cuisine_origin,
     meal_role, dish_structure, identity_level, sensory_profile,
     status, confidence_level)
  VALUES
    (${q(recipe.family)}, ${q(normalizeName(recipe.family))}, ${qn(recipe.cuisine_origin)}, ${qn(recipe.cuisine_origin)},
     'meal', ${qn(recipe.category)}, ${q(recipe.identity_level)}, ${qn(recipe.sensory.profile)},
     'candidate', ${q(recipe.confidence)})
  ON CONFLICT (canonical_name_normalized) DO UPDATE SET
    canonical_name = EXCLUDED.canonical_name,
    culinary_origin = EXCLUDED.culinary_origin,
    cuisine_origin = EXCLUDED.cuisine_origin,
    dish_structure = EXCLUDED.dish_structure,
    identity_level = EXCLUDED.identity_level,
    sensory_profile = EXCLUDED.sensory_profile,
    confidence_level = EXCLUDED.confidence_level,
    updated_at = now()
  RETURNING id INTO v_family;

  INSERT INTO culinary.recipe_versions
    (recipe_family_id, version_number, title, source_dataset_id, source_record_key,
     author_name, source_license, servings, prep_minutes, cook_minutes, difficulty,
     yield_quantity, yield_unit,
     quality_level, publication_status, content_hash,
     sensory_scores, dominant_flavors, aroma_families, target_textures,
     signature_ingredients, identity_guardrails, techniques, variant_candidates,
     allergens, conservation_text, planning_eligible, eligibility_issues)
  SELECT
    v_family, 3, ${q(recipe.family)}, ds.id, ${q(recipe.code)},
    'Myko', 'editorial', ${num(recipe.servings)}, ${num(recipe.prep_minutes)}, ${num(recipe.cook_minutes)}, ${qn(recipe.difficulty)},
    ${declaredYield ? num(declaredYield.quantity) : 'NULL'}, ${declaredYield ? q(declaredYield.unit) : 'NULL'},
    ${q(recipe.confidence)}, 'candidate', ${q(contentHash)},
    ${json(recipe.sensory.scores)}, ${array(recipe.sensory.dominant_flavors)},
    ${array(recipe.sensory.aroma_families)}, ${array(recipe.sensory.target_textures)},
    ${array(recipe.sensory.signature_ingredients)}, ${array(recipe.sensory.identity_guardrails)},
    ${array(recipe.techniques)}, ${array(recipe.variants)}, ${array(recipe.allergens)},
    ${qn(recipe.conservation)}, false, '[]'::jsonb
  FROM ops.source_datasets ds WHERE ds.code = 'myko_editorial_v3'
  ON CONFLICT (recipe_family_id, version_number) DO UPDATE SET
    title = EXCLUDED.title,
    source_dataset_id = EXCLUDED.source_dataset_id,
    source_record_key = EXCLUDED.source_record_key,
    servings = EXCLUDED.servings,
    prep_minutes = EXCLUDED.prep_minutes,
    cook_minutes = EXCLUDED.cook_minutes,
    difficulty = EXCLUDED.difficulty,
    yield_quantity = EXCLUDED.yield_quantity,
    yield_unit = EXCLUDED.yield_unit,
    quality_level = EXCLUDED.quality_level,
    publication_status = 'candidate',
    content_hash = EXCLUDED.content_hash,
    sensory_scores = EXCLUDED.sensory_scores,
    dominant_flavors = EXCLUDED.dominant_flavors,
    aroma_families = EXCLUDED.aroma_families,
    target_textures = EXCLUDED.target_textures,
    signature_ingredients = EXCLUDED.signature_ingredients,
    identity_guardrails = EXCLUDED.identity_guardrails,
    techniques = EXCLUDED.techniques,
    variant_candidates = EXCLUDED.variant_candidates,
    allergens = EXCLUDED.allergens,
    conservation_text = EXCLUDED.conservation_text,
    planning_eligible = false,
    eligibility_issues = '[]'::jsonb
  RETURNING id INTO v_version;

  DELETE FROM quality.review_tasks rt
  USING culinary.recipe_ingredient_requirements req
  WHERE req.recipe_version_id = v_version
    AND rt.entity_type = 'recipe_ingredient_requirement'
    AND rt.entity_id = req.id;
  DELETE FROM quality.review_tasks WHERE entity_type = 'recipe_version' AND entity_id = v_version;
  DELETE FROM culinary.recipe_steps WHERE recipe_version_id = v_version;
  DELETE FROM culinary.recipe_ingredient_requirements WHERE recipe_version_id = v_version;
  DELETE FROM culinary.recipe_instruction_branches WHERE recipe_version_id = v_version;
  DELETE FROM culinary.recipe_components WHERE recipe_version_id = v_version;
  DELETE FROM culinary.recipe_variation_axes WHERE recipe_family_id = v_family;

  INSERT INTO culinary.recipe_components (recipe_version_id, name, component_role, position)
  VALUES (v_version, 'plat', 'main', 1)
  RETURNING id INTO v_component;
`

  for (const [index, ingredient] of recipe.ingredients.entries()) {
    const normalized = normalizeName(ingredient.form)
    sql += `
  INSERT INTO culinary.recipe_ingredient_requirements
    (recipe_version_id, component_id, requirement_type, preferred_food_form_id,
     source_name, quantity, unit, strictness, culinary_role, is_optional, position)
  VALUES
    (v_version, v_component, 'exact_form',
     (SELECT ff.id FROM catalog.food_forms ff
      WHERE ff.canonical_name_normalized = ${q(normalized)} AND ff.status <> 'rejected'
      ORDER BY CASE ff.status WHEN 'published' THEN 0 ELSE 1 END, ff.confidence_level
      LIMIT 1),
     ${q(ingredient.form)}, ${num(ingredient.quantity)}, ${q(ingredient.unit)}, ${q(ingredient.optional ? 'optional' : 'required')},
     ${qn(ingredient.role)}, ${ingredient.optional ? 'true' : 'false'}, ${index + 1});
`
  }

  for (const step of recipe.steps) {
    sql += `
  INSERT INTO culinary.recipe_steps (recipe_version_id, step_number, instruction)
  VALUES (v_version, ${step.n}, ${q(step.instruction)});
`
  }

  sql += `
  INSERT INTO quality.review_tasks
    (entity_type, entity_id, task_type, priority, reason_codes, proposed_changes, status)
  SELECT 'recipe_ingredient_requirement', req.id, 'missing_food_form',
         CASE WHEN req.strictness = 'required' THEN 1 ELSE 4 END,
         ARRAY['exact_form_not_in_catalog'],
         jsonb_build_object('recipe_code', ${q(recipe.code)}, 'recipe_family', ${q(recipe.family)},
                            'position', req.position, 'strictness', req.strictness),
         'open'
  FROM culinary.recipe_ingredient_requirements req
  WHERE req.recipe_version_id = v_version AND req.preferred_food_form_id IS NULL;

  SELECT count(*) INTO v_missing
  FROM culinary.recipe_ingredient_requirements
  WHERE recipe_version_id = v_version
    AND strictness = 'required'
    AND preferred_food_form_id IS NULL;

  UPDATE culinary.recipe_versions
  SET planning_eligible = v_static_eligible AND v_missing = 0,
      eligibility_issues = CASE WHEN v_missing = 0
        THEN '[]'::jsonb
        ELSE jsonb_build_array(jsonb_build_object('code', 'unresolved_required_forms', 'count', v_missing))
      END
  WHERE id = v_version;

  IF v_missing > 0 THEN
    INSERT INTO quality.review_tasks
      (entity_type, entity_id, task_type, priority, reason_codes, proposed_changes, status)
    VALUES
      ('recipe_version', v_version, 'unresolved_required_ingredient', 1,
       ARRAY['blocking_form_missing'],
       jsonb_build_object('recipe_code', ${q(recipe.code)}, 'missing_required_count', v_missing), 'open');
  END IF;
`
  if (recipe.variants.length) {
    sql += `
  WITH axis AS (
    INSERT INTO culinary.recipe_variation_axes
      (recipe_family_id, name, selection_mode, required)
    VALUES (v_family, 'Variante culinaire', 'single', false)
    RETURNING id
  )
  INSERT INTO culinary.recipe_variation_options
    (variation_axis_id, name, confidence_level, status)
  SELECT axis.id, variant.name, ${q(recipe.confidence)}, 'candidate'
  FROM axis
  CROSS JOIN unnest(${array(recipe.variants)}) AS variant(name);

  INSERT INTO quality.review_tasks
    (entity_type, entity_id, task_type, priority, reason_codes, proposed_changes, status)
  VALUES
    ('recipe_version', v_version, 'variant_validation', 5,
     ARRAY['source_backed_variant_needs_test'], ${json({ recipe_code: recipe.code, variants: recipe.variants })}, 'open');
`
  } else {
    sql += `
  INSERT INTO quality.review_tasks
    (entity_type, entity_id, task_type, priority, reason_codes, proposed_changes, status)
  VALUES
    ('recipe_version', v_version, 'variant_enrichment', 6,
     ARRAY['variant_not_yet_sourced'],
     jsonb_build_object('recipe_code', ${q(recipe.code)}, 'policy', 'source_or_test_required'), 'open');
`
  }
  sql += `
  DELETE FROM ops.field_provenance
  WHERE entity_schema = 'culinary' AND entity_table = 'recipe_versions'
    AND entity_id = v_version AND field_name IN ('content', 'sensory_contract');
  INSERT INTO ops.field_provenance
    (entity_schema, entity_table, entity_id, field_name, source_dataset_id,
     source_record_key, normalized_value, transformation_rule, import_run_id, selected)
  SELECT 'culinary', 'recipe_versions', v_version, field_name, ds.id,
         ${q(`v3:${recipe.code}`)}, field_value, transformation_rule, run.id, true
  FROM ops.source_datasets ds
  JOIN LATERAL (
    VALUES
      ('content', to_jsonb(${q(contentHash)}::text), 'myko_v3_canonical_recipe'),
      ('sensory_contract', ${json(recipe.sensory)}, 'myko_v3_sensory_contract')
  ) AS provenance(field_name, field_value, transformation_rule) ON true
  JOIN LATERAL (
    SELECT id FROM ops.import_runs WHERE configuration_hash = ${q(corpusHash)} LIMIT 1
  ) run ON true
  WHERE ds.code = 'myko_editorial_v3';
END
$recipe$;
`
}

sql += `
-- Composition réelle : le croque-monsieur utilise une béchamel maison
-- dimensionnée à 240 g pour la recette de référence (4 personnes).
DO $composition$
DECLARE
  v_parent uuid;
  v_child uuid;
  v_component uuid;
BEGIN
  SELECT rv.id INTO v_parent
  FROM culinary.recipe_versions rv
  JOIN ops.source_datasets ds ON ds.id = rv.source_dataset_id
  WHERE ds.code = 'myko_editorial_v3' AND rv.source_record_key = 'FR-023';

  SELECT rv.id INTO v_child
  FROM culinary.recipe_versions rv
  JOIN ops.source_datasets ds ON ds.id = rv.source_dataset_id
  WHERE ds.code = 'myko_editorial_v3' AND rv.source_record_key = 'FR-024';

  IF v_parent IS NOT NULL AND v_child IS NOT NULL THEN
    INSERT INTO culinary.recipe_components
      (recipe_version_id, name, component_role, position, sub_recipe_version_id,
       required_quantity, required_unit)
    VALUES
      (v_parent, 'Béchamel maison', 'sauce', 2, v_child, 240, 'g')
    RETURNING id INTO v_component;

    UPDATE culinary.recipe_ingredient_requirements
    SET component_id = v_component,
        requirement_type = 'sub_recipe'
    WHERE recipe_version_id = v_parent
      AND source_name = 'Béchamel préparée';
  END IF;
END
$composition$;
`

const report = {
  corpus_version: corpus.corpus_version,
  corpus_hash: corpusHash,
  recipe_count: corpus.recipes.length,
  food_forms_total_normalized: foodReport.forms_total,
  recipes_static_eligible: foodReport.recipes_eligible_for_publication,
  v1_enriched_static_eligible: foodReport.v1_enriched_eligible_for_publication,
  planner_rule_count: corpus.planner_sensory_rules.length,
  sql_bytes: Buffer.byteLength(sql),
}

mkdirSync(OUT, { recursive: true })
writeFileSync(join(OUT, 'corpus-v3-load.sql'), sql)
const chunkDir = join(OUT, 'corpus-v3-chunks')
mkdirSync(chunkDir, { recursive: true })
const recipeBlocks = [...sql.matchAll(/DO \$recipe\$[\s\S]*?\$recipe\$;/g)]
const firstBlockAt = recipeBlocks[0]?.index ?? sql.length
const lastBlock = recipeBlocks.at(-1)
const tailAt = lastBlock ? lastBlock.index + lastBlock[0].length : sql.length
writeFileSync(join(chunkDir, '000-header.sql'), sql.slice(0, firstBlockAt))
recipeBlocks.forEach((match, index) => {
  writeFileSync(join(chunkDir, `${String(index + 1).padStart(3, '0')}-recipe.sql`), match[0])
})
writeFileSync(join(chunkDir, '999-composition.sql'), sql.slice(tailAt))
report.sql_chunk_count = recipeBlocks.length + 2

const sourceNameChunkDir = join(OUT, 'corpus-v3-source-name-chunks')
mkdirSync(sourceNameChunkDir, { recursive: true })
for (let start = 0; start < corpus.recipes.length; start += 25) {
  const recipes = corpus.recipes.slice(start, start + 25)
  const values = recipes.map((recipe) => `(${q(recipe.code)}, ${json(recipe.ingredients.map((ingredient) => ingredient.form))})`).join(',\n    ')
  const sourceNameSql = `WITH source_names(code, names) AS (\n  VALUES\n    ${values}\n), expanded AS (\n  SELECT source_names.code, ingredient.position::integer, ingredient.name\n  FROM source_names\n  CROSS JOIN LATERAL jsonb_array_elements_text(source_names.names)\n    WITH ORDINALITY AS ingredient(name, position)\n)\nUPDATE culinary.recipe_ingredient_requirements requirement\nSET source_name = expanded.name\nFROM culinary.recipe_versions version\nJOIN ops.source_datasets dataset ON dataset.id = version.source_dataset_id\nJOIN expanded ON expanded.code = version.source_record_key\nWHERE dataset.code = 'myko_editorial_v3'\n  AND requirement.recipe_version_id = version.id\n  AND requirement.position = expanded.position;\n`
  writeFileSync(join(sourceNameChunkDir, `${String(start / 25 + 1).padStart(2, '0')}.sql`), sourceNameSql)
}
report.source_name_chunk_count = Math.ceil(corpus.recipes.length / 25)
writeFileSync(join(OUT, 'corpus-v3-report.json'), `${JSON.stringify(report, null, 2)}\n`)
console.log(JSON.stringify(report, null, 2))
