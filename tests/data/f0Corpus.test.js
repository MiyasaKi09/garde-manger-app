/**
 * Tests Vitest — corpus alimentaire F0.
 * Réf. MYKO_DATA_FOUNDATION_V2 §3, §7.
 *
 * Lit data/foods/f0-corpus.json (réel) si présent, sinon le sample.
 * Vérifie :
 *  - Toutes les 31 formes R0 sont présentes dans le corpus.
 *  - Chaque forme a soit un ciqual_alim_code soit un nutrition_override.
 *  - Énergie + protéines non-nulles pour les formes non-condiments
 *    (les zéros mesurés de l'override comptent comme "présents").
 *  - Les protéines animales ont un safety_core_temp_c.
 *  - Chaque forme a ≥ 1 profil de conservation.
 *  - Chaque forme a un default_quantity_unit.
 *  - Les transformations référencent des formes existantes dans le concept.
 *  - Les confidences identity/category/state sont ≥ B pour toutes les formes.
 */
import { describe, it, expect } from 'vitest'
import { readFileSync, existsSync } from 'node:fs'
import { fileURLToPath } from 'node:url'
import { dirname, join } from 'node:path'
import { normalizeName } from '@/scripts/data/lib/normalize.mjs'

const root = join(dirname(fileURLToPath(import.meta.url)), '..', '..')
const realPath = join(root, 'data', 'foods', 'f0-corpus.json')
const samplePath = join(root, 'data', 'foods', 'f0-corpus.sample.json')
const corpusPath = existsSync(realPath) ? realPath : samplePath
const corpus = JSON.parse(readFileSync(corpusPath, 'utf8'))

// R0 functional forms list (generated by build-r0.mjs)
const r0Path = join(root, 'scripts', 'data', 'out', 'r0-functional-foods.json')
const r0 = existsSync(r0Path) ? JSON.parse(readFileSync(r0Path, 'utf8')) : null

// Confidence rank helper
const CONF_RANK = { 'D': 0, 'C': 1, 'B': 2, 'A': 3, 'A+': 4 }
const confRank = (c) => CONF_RANK[c] ?? -1

// Categories where missing energy/protein is expected (condiments, spices, herbs, beverages)
const SKIP_MACROS_CATEGORIES = new Set([
  'condiments_sauces', 'epices', 'herbes_aromates', 'boissons',
])

// Animal protein categories requiring safety_core_temp_c
const ANIMAL_PROTEIN_CATEGORIES = new Set([
  'viandes', 'volailles', 'poissons_fruits_de_mer', 'oeufs',
])

// Build a flat list of all forms with their concept
const allForms = []
const allFormsByNorm = new Map()

for (const concept of corpus.concepts) {
  for (const form of concept.forms) {
    const norm = normalizeName(form.canonical_name)
    const entry = { concept, form, norm }
    allForms.push(entry)
    allFormsByNorm.set(norm, entry)
  }
}

// ─────────────────────────────────────────────────────────────────────────────
describe('corpus F0 — structure de base', () => {
  it('corpus chargé depuis le bon fichier', () => {
    expect(existsSync(corpusPath)).toBe(true)
    expect(corpus.concepts).toBeDefined()
    expect(corpus.source.code).toBe('myko_f0_curated')
    expect(corpus.source.license).toBe('cc0-1.0')
  })

  it('au moins 27 concepts et 31 formes', () => {
    expect(corpus.concepts.length).toBeGreaterThanOrEqual(27)
    expect(allForms.length).toBeGreaterThanOrEqual(31)
  })

  it('chaque concept a canonical_name, category, identity_confidence, category_confidence', () => {
    for (const concept of corpus.concepts) {
      expect(concept.canonical_name, `canonical_name manquant`).toBeTruthy()
      expect(concept.category, `${concept.canonical_name}: category manquante`).toBeTruthy()
      expect(concept.identity_confidence, `${concept.canonical_name}: identity_confidence manquante`).toBeTruthy()
      expect(concept.category_confidence, `${concept.canonical_name}: category_confidence manquante`).toBeTruthy()
      expect(Array.isArray(concept.forms), `${concept.canonical_name}: forms doit être un tableau`).toBe(true)
      expect(concept.forms.length, `${concept.canonical_name}: au moins 1 forme`).toBeGreaterThanOrEqual(1)
    }
  })

  it('chaque forme a les champs obligatoires', () => {
    for (const { concept, form } of allForms) {
      const tag = `${concept.canonical_name}/${form.canonical_name}`
      expect(form.canonical_name, `${tag}: canonical_name manquant`).toBeTruthy()
      expect(form.default_quantity_unit, `${tag}: default_quantity_unit manquant`).toBeTruthy()
      expect(form.edible_yield_ratio, `${tag}: edible_yield_ratio manquant`).toBeTruthy()
      expect(form.state_confidence, `${tag}: state_confidence manquant`).toBeTruthy()
    }
  })
})

// ─────────────────────────────────────────────────────────────────────────────
describe('corpus F0 — couverture R0', () => {
  it('la liste R0 est disponible', () => {
    expect(r0, 'r0-functional-foods.json introuvable — lancer build-r0.mjs').not.toBeNull()
    expect(r0.count).toBeGreaterThanOrEqual(31)
  })

  it('toutes les formes R0 sont présentes dans le corpus F0', () => {
    if (!r0) return
    for (const req of r0.forms) {
      const found = allFormsByNorm.has(req.normalized)
      expect(found, `Forme R0 manquante dans F0 : « ${req.name} » (normalized: ${req.normalized})`).toBe(true)
    }
  })

  it('pas de forme R0 manquante (couverture 100 %)', () => {
    if (!r0) return
    const missing = r0.forms.filter((f) => !allFormsByNorm.has(f.normalized))
    expect(missing.length, `Formes R0 manquantes : ${missing.map((f) => f.name).join(', ')}`).toBe(0)
  })
})

// ─────────────────────────────────────────────────────────────────────────────
describe('corpus F0 — nutrition (CIQUAL ou override)', () => {
  it('chaque forme a soit ciqual_alim_code soit nutrition_override', () => {
    for (const { concept, form } of allForms) {
      const tag = `${concept.canonical_name}/${form.canonical_name}`
      const hasCiqual = form.ciqual_alim_code != null
      const hasOverride = form.nutrition_override != null
      expect(hasCiqual || hasOverride,
        `${tag}: ni ciqual_alim_code ni nutrition_override`).toBe(true)
    }
  })

  it('nutrition_override : les valeurs sont un objet et value_status est présent', () => {
    for (const { concept, form } of allForms) {
      if (!form.nutrition_override) continue
      const tag = `${concept.canonical_name}/${form.canonical_name}`
      const ov = form.nutrition_override
      expect(typeof ov.values).toBe('object')
      expect(ov.value_status, `${tag}: value_status manquant dans override`).toBeTruthy()
      // Les 4 macros principales doivent être présentes (même à zéro si mesurées)
      for (const macro of ['energy_kcal', 'protein_g', 'carbohydrate_g', 'fat_g']) {
        expect(Object.prototype.hasOwnProperty.call(ov.values, macro),
          `${tag}: macro ${macro} absente de nutrition_override.values`).toBe(true)
      }
    }
  })

  it('sel fin a les 4 macros à zéro (valeurs mesurées) + sodium', () => {
    const selFin = allFormsByNorm.get('sel fin')
    expect(selFin, 'sel fin introuvable dans le corpus').toBeDefined()
    expect(selFin.form.nutrition_override, 'sel fin doit avoir nutrition_override').toBeDefined()
    const v = selFin.form.nutrition_override.values
    expect(v.energy_kcal).toBe(0)
    expect(v.protein_g).toBe(0)
    expect(v.carbohydrate_g).toBe(0)
    expect(v.fat_g).toBe(0)
    expect(v.sodium_mg).toBeGreaterThan(0)
    expect(selFin.form.nutrition_override.value_status).toBe('measured')
  })

  it('les formes non-condiments ont nutrition_confidence ≥ C', () => {
    for (const { concept, form } of allForms) {
      if (SKIP_MACROS_CATEGORIES.has(concept.category)) continue
      const tag = `${concept.canonical_name}/${form.canonical_name}`
      expect(form.nutrition_confidence, `${tag}: nutrition_confidence manquante`).toBeTruthy()
      expect(confRank(form.nutrition_confidence),
        `${tag}: nutrition_confidence ${form.nutrition_confidence} < C`).toBeGreaterThanOrEqual(confRank('C'))
    }
  })
})

// ─────────────────────────────────────────────────────────────────────────────
describe('corpus F0 — sécurité alimentaire', () => {
  it('les protéines animales portent safety_core_temp_c', () => {
    for (const { concept, form } of allForms) {
      if (!ANIMAL_PROTEIN_CATEGORIES.has(concept.category)) continue
      const tag = `${concept.canonical_name}/${form.canonical_name}`
      expect(form.safety_core_temp_c, `${tag}: safety_core_temp_c manquant pour protéine animale`).toBeGreaterThan(0)
    }
  })

  it('les températures à cœur sont dans les plages sanitaires (60-85 °C)', () => {
    for (const { concept, form } of allForms) {
      if (!form.safety_core_temp_c) continue
      const tag = `${concept.canonical_name}/${form.canonical_name}`
      expect(form.safety_core_temp_c,
        `${tag}: safety_core_temp_c ${form.safety_core_temp_c} hors plage [60, 85]`
      ).toBeGreaterThanOrEqual(60)
      expect(form.safety_core_temp_c).toBeLessThanOrEqual(85)
    }
  })
})

// ─────────────────────────────────────────────────────────────────────────────
describe('corpus F0 — conservation', () => {
  it('chaque forme a ≥ 1 profil de conservation', () => {
    for (const { concept, form } of allForms) {
      const tag = `${concept.canonical_name}/${form.canonical_name}`
      expect(Array.isArray(form.storage) && form.storage.length >= 1,
        `${tag}: aucun profil de conservation`).toBe(true)
    }
  })

  it('chaque profil de conservation a les champs obligatoires', () => {
    for (const { concept, form } of allForms) {
      for (const s of form.storage || []) {
        const tag = `${concept.canonical_name}/${form.canonical_name}`
        expect(s.storage_method, `${tag}: storage_method manquant`).toBeTruthy()
        expect(s.shelf_life_max_hours, `${tag}: shelf_life_max_hours manquant`).toBeGreaterThan(0)
        expect(s.recommended_hours, `${tag}: recommended_hours manquant`).toBeGreaterThan(0)
        expect(s.safety_level, `${tag}: safety_level manquant`).toBeTruthy()
        expect(s.confidence, `${tag}: confidence manquante dans storage`).toBeTruthy()
      }
    }
  })
})

// ─────────────────────────────────────────────────────────────────────────────
describe('corpus F0 — conversions d\'unités', () => {
  it('chaque conversion a from_unit, to_unit, factor > 0', () => {
    for (const { concept, form } of allForms) {
      for (const conv of form.conversions || []) {
        const tag = `${concept.canonical_name}/${form.canonical_name}`
        expect(conv.from_unit, `${tag}: from_unit manquant`).toBeTruthy()
        expect(conv.to_unit, `${tag}: to_unit manquant`).toBeTruthy()
        expect(conv.factor, `${tag}: factor ≤ 0`).toBeGreaterThan(0)
        expect(conv.confidence, `${tag}: confidence manquante dans conversion`).toBeTruthy()
      }
    }
  })

  it('les formes avec unité "u" ont au moins une conversion u→g', () => {
    for (const { concept, form } of allForms) {
      if (form.default_quantity_unit !== 'u') continue
      const tag = `${concept.canonical_name}/${form.canonical_name}`
      const hasUtoG = (form.conversions || []).some(
        (c) => c.from_unit === 'u' && c.to_unit === 'g'
      )
      expect(hasUtoG, `${tag}: unité 'u' sans conversion u→g`).toBe(true)
    }
  })
})

// ─────────────────────────────────────────────────────────────────────────────
describe('corpus F0 — transformations', () => {
  it('les transformations référencent des formes existant dans le concept', () => {
    for (const concept of corpus.concepts) {
      for (const tx of concept.transformations || []) {
        const srcNorm = normalizeName(tx.source_form)
        const srcExists = concept.forms.some((f) => normalizeName(f.canonical_name) === srcNorm)
        expect(srcExists,
          `${concept.canonical_name}: forme source de transformation introuvable : « ${tx.source_form} »`
        ).toBe(true)
        expect(tx.action_code, `${concept.canonical_name}: action_code manquant`).toBeTruthy()
        expect(tx.action_label, `${concept.canonical_name}: action_label manquant`).toBeTruthy()
        expect(Array.isArray(tx.outputs), `${concept.canonical_name}: outputs doit être un tableau`).toBe(true)
      }
    }
  })

  it('les outputs de transformation ont un rôle et un rendement valide', () => {
    for (const concept of corpus.concepts) {
      for (const tx of concept.transformations || []) {
        for (const out of tx.outputs || []) {
          const tag = `${concept.canonical_name}/${tx.action_code}`
          expect(out.form, `${tag}: output.form manquant`).toBeTruthy()
          expect(out.role, `${tag}: output.role manquant`).toBeTruthy()
          expect(out.expected_yield_ratio, `${tag}: output.expected_yield_ratio invalide`).toBeGreaterThan(0)
          expect(out.expected_yield_ratio).toBeLessThanOrEqual(1)
        }
      }
    }
  })
})

// ─────────────────────────────────────────────────────────────────────────────
describe('corpus F0 — niveaux de confiance (publish guardrail)', () => {
  it('toutes les formes ont identity_confidence ≥ B', () => {
    for (const { concept, form } of allForms) {
      const tag = `${concept.canonical_name}/${form.canonical_name}`
      const idConf = form.identity_confidence ?? concept.identity_confidence
      expect(confRank(idConf),
        `${tag}: identity_confidence ${idConf} < B`
      ).toBeGreaterThanOrEqual(confRank('B'))
    }
  })

  it('toutes les formes ont category_confidence ≥ B', () => {
    for (const { concept, form } of allForms) {
      const tag = `${concept.canonical_name}/${form.canonical_name}`
      const catConf = form.category_confidence ?? concept.category_confidence
      expect(confRank(catConf),
        `${tag}: category_confidence ${catConf} < B`
      ).toBeGreaterThanOrEqual(confRank('B'))
    }
  })

  it('toutes les formes ont state_confidence ≥ B', () => {
    for (const { concept, form } of allForms) {
      const tag = `${concept.canonical_name}/${form.canonical_name}`
      expect(confRank(form.state_confidence),
        `${tag}: state_confidence ${form.state_confidence} < B`
      ).toBeGreaterThanOrEqual(confRank('B'))
    }
  })
})

// ─────────────────────────────────────────────────────────────────────────────
describe('corpus F0 — intégrité du chargeur SQL', () => {
  it('les noms normalisés sont uniques par concept (pas de doublon)', () => {
    for (const concept of corpus.concepts) {
      const norms = concept.forms.map((f) => normalizeName(f.canonical_name))
      const unique = new Set(norms)
      expect(unique.size, `${concept.canonical_name}: formes dupliquées dans le concept`).toBe(norms.length)
    }
  })

  it('les noms normalisés de concepts sont globalement uniques', () => {
    const norms = corpus.concepts.map((c) => normalizeName(c.canonical_name))
    const unique = new Set(norms)
    expect(unique.size).toBe(norms.length)
  })
})
